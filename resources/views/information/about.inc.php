      <h1>About Us</h1>
			<p>Silverados is a locally owned and operated independent cinema in York, Western Australia.</p>
      <p>It opened in 1957, when Giovanni Silverado, and Italian immigrant trained as a projectionist,
         realised that the then small town of York lacked a cinema, or for that matter, and major forms of entertainment.</p>
      <p>Silverados has remained a family owned and operated business spanning three generations, and now, in 2015,
         Silverados is proud to have undertaken major renovations to present our new, fully-refurbished cinema complex,
        featuring:</p>
      <ul>
				<li>Digital 3D Projection</li>
				<li>Dolby Digital Sound</li>
				<li>12 First Class Seates</li>
				<li>13 Bean Bag Seats (Seats 2 People)</li>
				<li>40 Comfortable Standard Seates</li>
      </ul>
